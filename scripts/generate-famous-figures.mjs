function main() {
  const dataPath = join(root, "famous-figures", "figures-data.json");
  const outPath = join(root, "famous-figures", "famous-figures.yaml");
  const outDir = dirname(outPath);

  let base = [];
  if (existsSync(dataPath)) {
    base = JSON.parse(readFileSync(dataPath, "utf8"));
  }
  const extendedPath = join(root, "famous-figures", "figures-data-extended.json");
  let extended = [];
  if (existsSync(extendedPath)) {
    extended = JSON.parse(readFileSync(extendedPath, "utf8"));
  }

  const all = [...base, ...EXTENDED, ...extended];
  const seen = new Set();
  const deduped = all.filter((f) => {
    const id = slugify(f.name);
    if (seen.has(id)) return false;
    seen.add(id);
    return true;
  });

  const figures = deduped.map((f) => ({
    id: slugify(f.name),
    name: f.name,
    description: f.description || "",
    category: f.category || "philosophy",
  }));

  if (!existsSync(outDir)) {
    mkdirSync(outDir, { recursive: true });
  }

  const output = {
    categories: CATEGORIES,
    figures,
  };

  writeFileSync(outPath, yaml.stringify(output, { lineWidth: 0 }), "utf8");
  console.log(`Wrote ${figures.length} figures to ${outPath}`);
}

main();
